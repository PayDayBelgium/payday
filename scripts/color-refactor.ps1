$root = "C:\Development\PayDay\payday-web\src"
$files = Get-ChildItem -Path $root -Recurse -Include "*.tsx","*.ts" -File
$changedCount = 0
$replacementsTotal = 0

foreach ($file in $files) {
    $content = Get-Content -Path $file.FullName -Raw
    $original = $content
    $fileReps = 0
    foreach ($map in $mappings) {
        $from = $map[0]
        $to = $map[1]
        $before = $content
        $content = $content.Replace($from, $to)
        if ($content -ne $before) {
            $count = ([regex]::Matches($before, [regex]::Escape($from))).Count
            $fileReps += $count
        }
    }
    if ($content -ne $original) {
        Set-Content -Path $file.FullName -Value $content -NoNewline -Encoding utf8
        $changedCount++
        $replacementsTotal += $fileReps
    }
}

Write-Output "Files changed: $changedCount"
Write-Output "Total replacements: $replacementsTotal"
